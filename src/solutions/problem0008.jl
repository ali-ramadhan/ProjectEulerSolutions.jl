Find the thirteen adjacent digits in the 1000-digit number that have the greatest product.
What is the value of this product?

## Solution approach

The problem requires finding the maximum product of 13 consecutive digits in the given
1000-digit number.
The approach uses a sliding window technique:
1. Iterate through all possible 13-digit substrings
2. Skip any substring containing '0' (as it would make the product zero)
3. Calculate the product of digits for each valid substring
4. Keep track of the maximum product found

## Complexity analysis

Time complexity: O(n × k)
- n is the length of the number string (1000 digits)
- k is the window size (13 digits)
- We iterate through (n-k+1) positions, and for each position we calculate the product of
