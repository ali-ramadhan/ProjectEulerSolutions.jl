"""
    product_of_digits(str)

Calculate the product of all digits in the given string.
"""
function product_of_digits(str)
    prod = 1
    for c in str
        digit = parse(Int, c)
        prod *= digit
    end
    return prod
end

"""
    largest_product_in_series(num_digits)

Find the greatest product of num_digits adjacent digits in the 1000-digit number.
Returns the product value and the substring containing those digits.
"""
function largest_product_in_series(num_digits)
    max_product = 0
    max_substring = ""

    for i in 1:(length(BIG_NUMBER) - num_digits + 1)
        substring = BIG_NUMBER[i:(i + num_digits - 1)]

        if '0' in substring
            continue
        end

        product = product_of_digits(substring)

        if product > max_product
            max_product = product
            max_substring = substring
        end
    end

    return max_product, max_substring
end

function solve()
    product, substring = largest_product_in_series(13)
    @info "Found 13 consecutive digits with largest product: " *
          "$(join(collect(substring), " × ")) = $product"
    return product
end

